package com.DT.java;

public class DataTypes {

	public static void main(String[] args) {
		
		int i = 4523;   //can store 32 bit interger values only
		long l = 652345; //can store 64 bit integer values only
		double d1 = 56.3453; //can store 64 bit decimal values only
		double d2 = 123456; //can use it to interger values too
		char c = 'k'; //can store single char only
		boolean b = true;//it can store boolen values like true or false
		String str = "Hello world";//it can store any string values
		
		System.out.println("Integer Var Is--->"+i);
		System.out.println("Long Var Is---->"+l);
		System.out.println("Double Var d1 Is--->"+d1);
		System.out.println("Double Var d2 Is--->"+d2);
		System.out.println("char Var c Is --> "+c);
		System.out.println("boolean Var b Is --> "+b);
		System.out.println("String Var str Is --> "+str);
	}

}
